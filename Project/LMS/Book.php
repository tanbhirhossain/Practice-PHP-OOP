class Book {

    private $id;
    private $title;
    private $isAvailable = true;

    public function __construct($id, $title){
        $this->id = $id;
        $this->title = $title;
    }

    public function getTitle(){
        return $this->title;
    }

    public function borrowBook(){
        if($this->isAvailable){
            $this->isAvailable = false;
            return "Avilable to borrow!";
        }
        return "We are sorry, this book is not avialable at this moment!";
    }

    public function returnBook(){
        if($this->isAvailable == false){
            $this->isAvailable = true;
            return "Your book is returned";
        }
        return "This book is Avialable";
    }
    
}


